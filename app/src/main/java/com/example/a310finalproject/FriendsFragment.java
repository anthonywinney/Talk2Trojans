    private void generateFriends() {
        GridView friendGrid = getView().findViewById(R.id.friend_grid);
        // Retrieve friends from DB and store in friends
        List<String> friendsNames = Arrays.asList(CurrentUser.currentUser.getmFriends().split(","));
        if(friendsNames.size() == 1 && friendsNames.get(0).equals("")) {
            friendsNames = new ArrayList<>();
        }
        ArrayList<Friend> friends = new ArrayList<>();
        if(friendsNames != null) {
            for (String friendName : friendsNames) {
                friends.add(new Friend(friendName));
            }
        }


        FriendViewAdapter adapter = new FriendViewAdapter(getContext(), friends);
        friendGrid.setAdapter(adapter);
    }

    @Override
    public void onStart() {
        super.onStart();
        generateFriends();
    }
